public class Prashanth_while {
    public static void main(String[] args) {
        int i = 0 ;
        while(i < 10){
            
            if(i>10){
                System.out.println("Out of loop");
            }
            else{
                System.out.println("Running loop");
            }
            i++;
        }
           
    }
    
}
